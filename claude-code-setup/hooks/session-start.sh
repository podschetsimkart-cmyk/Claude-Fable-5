#!/usr/bin/env bash
# SessionStart-хук: если в проекте нет CLAUDE.md — создаёт стартовый шаблон,
# чтобы каждая новая сессия начиналась с файла-справки о проекте.
set -u

dir="${CLAUDE_PROJECT_DIR:-.}"
[ -f "$dir/CLAUDE.md" ] && exit 0
[ -w "$dir" ] || exit 0

cat > "$dir/CLAUDE.md" <<'EOF'
# CLAUDE.md

<!-- Создано автоматически хуком session-start. Заполни по факту проекта. -->

## Проект
- Что делает: <одной строкой>
- Стек: <aiogram 3 / FastAPI / Apps Script / ...>
- Точка входа: <main.py>

## Команды
- Тесты: `pytest -q`
- Линтер: `ruff check .`
- Запуск: <команда>

## Правила проекта
- Секреты только в .env (в .gitignore).
- <конвенции проекта>

## Не делать без явной просьбы
- <запреты>
EOF

echo "В проекте не было CLAUDE.md — создан шаблон. Заполни его секции реальными фактами проекта (стек, команды, правила) при первой возможности."
